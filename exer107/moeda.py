def aumentar(v):
    valor_aum = v + (v * 0.1)
    print(f'O valor R${v} acrecido de 10% é de R${valor_aum}')

def diminuir(v):
    valor_dim = v - (v * 0.1)
    print(f'O valor R${v} acrecido de 10% é de R${valor_dim}')

def dobro(v):
    dobro = v * 2
    print(f'O dobro de R${v} é R${dobro}')

def metade(v):
    met = v / 2
    print(f'A metade de R${v} é R${met}')